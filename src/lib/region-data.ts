// CSV 파일에서 파싱된 지역 데이터 타입
export interface RegionData {
  [sido: string]: {
    [sigungu: string]: string[]
  }
}

// CSV 행 데이터 타입
interface CsvRow {
  법정동코드: string
  시도명: string
  시군구명: string
  읍면동명: string
  리명: string
  순위: string
  생성일자: string
  삭제일자: string
  과거법정동코드: string
}

// CSV 파일을 파싱하여 regionData 구조로 변환하는 함수
export function parseRegionDataFromCsv(csvContent: string): RegionData {
  const lines = csvContent.split('\n')
  const headers = lines[0].split(',')
  const data: RegionData = {}

  // 헤더를 제외한 데이터 라인들을 처리
  for (let i = 1; i < lines.length; i++) {
    const line = lines[i].trim()
    if (!line) continue

    const values = line.split(',')
    const row: CsvRow = {
      법정동코드: values[0] || '',
      시도명: values[1] || '',
      시군구명: values[2] || '',
      읍면동명: values[3] || '',
      리명: values[4] || '',
      순위: values[5] || '',
      생성일자: values[6] || '',
      삭제일자: values[7] || '',
      과거법정동코드: values[8] || '',
    }

    // 삭제된 지역은 제외
    if (row.삭제일자) continue

    const sido = row.시도명
    const sigungu = row.시군구명
    const dong = row.읍면동명

    if (!sido || !sigungu || !dong) continue

    // 시도가 없으면 생성
    if (!data[sido]) {
      data[sido] = {}
    }

    // 시군구가 없으면 생성
    if (!data[sido][sigungu]) {
      data[sido][sigungu] = []
    }

    // 동이 중복되지 않도록 추가
    if (!data[sido][sigungu].includes(dong)) {
      data[sido][sigungu].push(dong)
    }
  }

  return data
}

